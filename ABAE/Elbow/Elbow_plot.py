cos_sim={}


def sim_meanavg(val_split):
    mean_avg=val_split[1]
    aspect_size= val_split[0].split(': ([')[0]
    sim=val_split[0].split(': ([')[1]
    sim=sim.split(', ')
    floatsim=[float(i) for i in sim]
    cos_sim[aspect_size]=floatsim
    #print(cos_sim)
    return mean_avg

